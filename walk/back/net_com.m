% --- Executes on button press in pushbutton1.
function pushbutton1_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton1 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
global t3;
global t4;
global t5;
global f;
global ListenTimer;
global startflag;
global stopflag;
global paravalue;
temp=isempty(startflag);
if startflag==0||temp==1
stopflag=0;
para_config();
paravalue=ini2struct('Config.ini');
diary('c:\web\resources\testlog3.txt');
% diary('d:\radar\output\testlog3.txt');
diary on;
datestr(now,31)
% 连接中储雷达数据存储FTP服务器
str1 = num2str(paravalue.ftp.ftpip);
str2 = num2str(paravalue.ftp.ftpuser);
str3 = num2str(paravalue.ftp.ftppw);
str4 = num2str(paravalue.ftp.ftpport);
str=strcat(str1,':',str4);
disp('连接中储雷达数据存储FTP服务器......');
f=ftp(str,str2,str3);
disp('连接已成功......');
pasv(f);

% 连接VC雷达数据采集服务器
str1 = num2str(paravalue.vcserver.vcip);
str2 = str2num(paravalue.vcserver.vcport);
t4 = tcpip(str1,str2);       
set(t4,'InputBufferSize',100);
t4.BytesAvailableFcnMode = 'byte';
t4.BytesAvailableFcnCount = 1;
t4.BytesAvailableFcn = @netcallback3;
disp('连接VC雷达数据采集服务器......');
connectvc();
disp('VC服务器连接成功......');

% 建立与云平台通信的服务器端
str1 = str2num(paravalue.localserver.localport);
t3 = tcpip('0.0.0.0',str1,'NetworkRole','server');
set(t3,'Timeout',10);                                                       %socket连接超时时间，是fread读取的阻塞时间，超过此时间未读到数据则退出fread
set(t3,'InputBufferSize',100);
t3.BytesAvailableFcnMode = 'byte';
t3.BytesAvailableFcnCount = 1;
t3.BytesAvailableFcn = @tcpserverback;
disp('建立与云平台通信的服务器端......');
fopen(t3);
disp('云平台客户端已连接......');

% 初始化连接云平台服务器参数
str1 = num2str(paravalue.cloudserver.cloudserverip);
str2 = str2num(paravalue.cloudserver.cloudport);
t5 = tcpip(str1,str2);       
set(t5,'InputBufferSize',100);
t5.BytesAvailableFcnMode = 'byte';
t5.BytesAvailableFcnCount = 1;
disp('初始化连接云平台服务器参数......');

ListenTimer = timer('TimerFcn',@ListenTimerCallback, 'Period', 3.0, 'ExecutionMode','fixedDelay','BusyMode','drop'); % 
disp('打开定时器监听端口......');
start(ListenTimer);
startflag=1;
disp('系统已开启......');
else 
    disp('系统已开启，请不要重复点击......');
end

